        public override string ToString()
        {
            var sb = new StringBuilder();
            foreach (ReturnFieldInfo returnFieldInfo in Values) sb.AppendLine(returnFieldInfo.ToString());
            return sb.ToString();
        }

        public void Add(ReturnFieldInfo info)
        {
            string key = info.ReturnFieldId;
            if (ContainsKey(key))
            {
                this[key] = info;
            }
            else
            {
                Add(key, info);
            }
        }
